
/*
【React还是Vue：你该如何选择？】参考：https://zhuanlan.zhihu.com/p/137046152
 * 大型复杂的项目
    答案：选 React
    原因：
        * React 更适合大型复杂的项目，大型复杂的项目在遇到问题的时候，调试起来是一个比较头疼的问题，
          React是基于Immutabled不可变状态来设计的，这样的话在进行开发调试的时候，就可以进行前后状态数据的对比来快速定位问题。
        * React 开发起来更加灵活，一些复杂的逻辑开发起来更方便，也可以更加灵活的进行组件的组合开发。
 * 如果你想要一个同时适用于Web端和原生APP的框架
    答案：选 React
    原因：如果你学过React.js，很快就能上手React Native
 * 如果你想要最大的生态系统
    答案：选 React

 * 如果你喜欢用模板搭建应用（或者有这个想法）
    答案：选 Vue
*/